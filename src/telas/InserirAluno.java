            complemento.setSerieAnterior(campoSerieAnterior.getText());
            complemento.setObservacaoAnterior(campoObservacaoAluno.getText());
            complemento.setIdAluno(Integer.parseInt(campoId.getText()));

            Dao dao = new Dao();
            dao.inserirAluno(aluno);
            dao.inserirDadosComplementares(complemento);
