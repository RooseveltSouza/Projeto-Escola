            aluno.setSerie((String) campoSerie.getSelectedItem());
            aluno.setModalidade((String) campoModalidade.getSelectedItem());
            aluno.setTurma((String) campoTurma.getSelectedItem());
            aluno.setEmail(campoEmail.getText());
            aluno.setTransporteEscolar((String) campoVeiculoEscolar.getSelectedItem());
            aluno.setLocalidade(campoLocalidade.getText());

            //dados do resposavel
            complemento.setNomeResponsavel(campoNomeResponsavel.getText());
            complemento.setParentesco(campoParentesco.getText());
            complemento.setEnderecoResponsavel(campoEnderecoResponsavel.getText());
            complemento.setMunicipio(campoMunicipio.getText());
            complemento.setUf((String) campoUf.getSelectedItem());
            complemento.setCep(campoCep.getText());
            complemento.setTelefone(campoTelefone.getText());
            complemento.setEmailParentesco(campoEmailResponsavel.getText());

            // dados escolar anterior
            complemento.setEscolaAnterior(campoEscolaAnterior.getText());
            complemento.setMunicipioAnterior(campoMunicipioAnterior.getText());
            complemento.setUfAnterior((String) campoUfAnterior.getSelectedItem());
